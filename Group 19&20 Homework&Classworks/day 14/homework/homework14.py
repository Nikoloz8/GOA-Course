number = int(input("Say random number: "))

if (number // 2) * 2 == number:    #"//" - ეს არის გაყოფის ოპერატორი, თუმცა "/" - სგან განსხვავებით მას არ შეუძლია float-ის დაბრუნება,
    print("Your number is even!")  # ამის ნაცვლად იგი ჩამოაშორებს ათწილადს არამთელ ნაწილს და გამოიტანს მთელ რიცხვს (არ მოამრგვალებს!).
                                
else:
    print("Your number is odd!")